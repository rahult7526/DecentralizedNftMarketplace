export function getContractAddress(chainId: number): string {
  const addresses = CONTRACT_ADDRESSES[chainId as keyof typeof CONTRACT_ADDRESSES]
  return addresses?.nft || CONTRACT_ADDRESSES[31337].nft
}

export function getMarketplaceAddress(chainId: number): string {
  const addresses = CONTRACT_ADDRESSES[chainId as keyof typeof CONTRACT_ADDRESSES]
  return addresses?.marketplace || CONTRACT_ADDRESSES[31337].marketplace
}

